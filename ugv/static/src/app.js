document.addEventListener("keyup", function (event) {
  if (event.key == "ArrowLeft") {
    console.log("left");
  }
  if (event.key == "ArrowRight") {
    console.log("right");
  }
  if (event.key == "ArrowUp") {
    console.log("up");
  }
  if (event.key == "ArrowDown") {
    console.log("down");
  }
});